#include "libft.h"

void	*ft_memset(void *b, int c, size_t len) // must read more about memory
{
	size_t	i;
	char	*str;

	str = (char *)b;
	i = 0;
	while (i < len)
	{
		str[i] = c;
		i++;
	}
	return (b);
}
